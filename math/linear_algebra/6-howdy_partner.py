#!/usr/bin/env python3
"""
This module provides a function to concatenate two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays into a new list.

    Args:
        arr1 (list): The first array.
        arr2 (list): The second array.

    Returns:
        list: A new list that contains the elements of arr1
              followed by the elements of arr2.
    """
    return arr1 + arr2
